    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $operation = new Operation();
        $operation->date_livraison = $request->get('date_livraison');
        $operation->type = $request->get('type');
        $operation->etat_livraison  = $request->get('etat_livraison');
        $operation->adresse_livraison  = $request->get('adresse_livraison');
        
        $operation->client()->associate(auth()->user()->id);
        $operation->save();
    }

